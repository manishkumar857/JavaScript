const age = 22

switch(age){
    case 2:
        console.log("2")
        break;
    case 21:
        console.log("21")
        break;
    case 23:
        console.log("23")
        break;
    case 22:
        console.log("22")
        break;
    
    default:
        console.log("default case")
        break;
}

// if we dont write break it will execute all the code below after match except DEFAULT CASE